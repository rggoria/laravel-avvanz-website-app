    <p class="fw-bolder">
        What is APRA?
    </p>
    <p>
        APRA stands for Australian Prudential Regulation Authority – A regulatory body that sets requirements for companies operating in Australia to ensure the safety and stability of the financial system. 
    </p>
    <p>
        In March 2004, APRA issued a consultation paper on proposed Fit and Proper reforms regarding <span class="fw-bolder">Responsible Persons</span>. A Responsible Person is defined by APRA based on the responsibilities and activities of the role and is generally an individual who is “responsible for the management and oversight of a regulated institution/RSE Licensee’s business operations”. Depending on the type of institution, a Responsible Person may hold a position as director, secretary, senior manager, appointed auditor, appointed actuary or other role where the activities undertaken as part of that role “may materially affect the whole, or a substantial part, of the business of the regulated institution or its financial standing”. It should be noted that individuals who are not employees of a regulated institution can also be Responsible Persons if they hold such a role.
    </p>
    <p>
        More information on APRA – <a href="https://www.apra.gov.au/" class="link-hover">https://www.apra.gov.au/</a>
    </p>
