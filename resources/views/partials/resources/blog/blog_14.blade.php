    <p class="fw-bolder">
        The FCRA
    </p>
    <p>
        The Fair Credit Reporting Act (<a href="https://www.ftc.gov/legal-library/browse/statutes/fair-%20credit-reporting-act" class="link-hover">https://www.ftc.gov/legal-library/browse/statutes/fair- credit-reporting-act</a>), or FCRA, is a US federal law regulating several aspects of the consumer reporting industry. The FCRA is not just limited to governing credit reports; it also governs information provided by background screening companies for broader information provided, as well as companies that use their services. It aims to ensure the fairness of the information recorded in consumer’s files and used with these entities. Predominant in the FCRA is the term “consumer report”. A consumer report is more than a credit report. It is a report from a Consumer Reporting Agency (“CRA”) that provides information on an individual’s credit history, character, reputation. Such a report would be used to determining this person’s eligibility for
    </p>
    <p>
        (A) credit or insurance to be used primarily for personal, family, or household purposes;
    </p>
    <p>
        (B) employment purposes; or
    </p>
    <p>
        (C) any other purpose authorized under section 604 [§1681b] of the FCRA. Whether you are screening for employment purposes, gig economy, tenant screening, or board memberships, these activities will often fall under the requirements of the FCRA.
    </p>
    <p class="fw-bolder">
        FCRA and Background Checks
    </p>
    <p>
        The FCRA has a significant impact on background screening because background checks involve collection and use of consumer credit information and employers use background checks to assess the suitability of job candidates, tenants and other individuals. These background checks can include information about a person’s criminal history, employment history, education and credit history. More information can be found here:<a href="{{ route('screen-sub-2') }}" class="link-hover">https://www.avvanz.com/background-checks/</a>
    </p>
    <p>
        The FCRA imposes specific duties on users of consumer reports such as employers and other entities. It also imposes duties on entities involved in producing consumer reports, such as background screeners or the furnishers of information. These requirements include:
    </p>
    <ul>
        <li>
            specific language for the notice and authorization of the background screen
        </li>
        <li>
            notification to the consumer when potential adverse action will occur based upon the information found in the background screening report, and notification when such adverse action will occur
        </li>
        <li>
            the ability of the consumer to dispute information provided in the consumer report
        </li>
        <li>
            the ability of the consumer to obtain information from organizations about what information is held on them
        </li>
    </ul>
    <p>
        In addition to the FCRA, other regulations, such as “Ban the Box” regulations and restrictions on the use of credit and criminal record reports, may govern the use of background screening information in decision making for an individual user. The requirements are complex and advice from legal counsel is advised.
    </p>
    <p>
        In a nutshell, FCRA sets important standards for the collection, use, and sharing of consumer credit information in the context of background screening. Companies that perform background checks must comply with these requirements to ensure that their practices are fair, accurate and respectful of consumers’ rights.
    </p>
