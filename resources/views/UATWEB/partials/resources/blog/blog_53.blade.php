    <p>
        In Australia, minor offenses may be <span class="fw-bolder">spent after five to ten years</span>, while serious crimes can be reported indefinitely, depending on the state. The <span class="fw-bolder">National Police Check</span> is commonly used to access a person’s criminal record.
