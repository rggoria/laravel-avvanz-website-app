    <p class="fw-bolder">
        What is a DBS Check?
    </p>
    <p>
        A DBS check, which stands for Disclosure and Barring Service check, is a background check conducted by the UK government to assess an individual’s criminal record history. The DBS check provides information about any past convictions, warnings, reprimands, or cautions the individual may have received.
    </p>
    <p>
        By conducting a DBS check, employers can gain insight into an applicant’s suitability for a specific role, especially those involving working with children or vulnerable adults. The check helps employers make informed decisions to protect the safety and well-being of those under their care.
    </p>
    <p class="fw-bolder">
        Why is a DBS Check Necessary for Certain Jobs?
    </p>
    <p>
        The requirement for a DBS check is determined by the nature of the job and the level of contact an individual will have with vulnerable groups. It is necessary for certain roles to help prevent individuals with a history of criminal behavior from working in positions where they may pose a risk.
    </p>
    <p>
        For example, jobs in healthcare, education, social services, and childcare often require a DBS check due to the nature of the work and the potential vulnerability of the people being served. By conducting a DBS check, employers can ensure that they are providing a safe environment for their clients, patients, or students.
    </p>
    <p>
        DBS checks are available at different levels, each suited to different roles and responsibilities. The three main levels of DBS checks are:
    </p>
    <ul>
        <li>
            Basic DBS Check: Provides information on unspent convictions and is suitable for roles with minimal contact with vulnerable groups. 
        </li>
        <li>
            Standard DBS Check: Provides information on both spent and unspent convictions, as well as cautions, reprimands, and final warnings. It is required for roles with regular contact with vulnerable groups, such as teaching or healthcare professions.
        </li>
        <li>
            Enhanced DBS Check: Provides the most comprehensive level of information, including the same details as a standard check, as well as any relevant additional police information. It is necessary for positions that involve substantial contact with vulnerable groups, such as working in childcare or caring for vulnerable adults. 
        </li>
    </ul>
